@app.route("/dashboard")
def home():

    conn = sqlite3.connect("health.db")
    df = pd.read_sql_query("SELECT * FROM patients", conn)
    conn.close()

    search = request.args.get("search", "")

    if search:
        df = df[
            df["Patient_ID"].astype(str).str.contains(search, case=False)
            | df["Gender"].astype(str).str.contains(search, case=False)
        ]

    df["Height_m"] = df["Height_cm"] / 100
    df["BMI"] = (df["Weight_kg"] / (df["Height_m"] ** 2)).round(2)

    def bmi_category(bmi):
        if bmi < 18.5:
            return "Underweight"
        elif bmi < 25:
            return "Normal"
        elif bmi < 30:
            return "Overweight"
        else:
            return "Obese"

    df["BMI_Category"] = df["BMI"].apply(bmi_category)

    def bp_status(sys, dia):
        if sys < 120 and dia < 80:
            return "Normal"
        elif sys < 130 and dia < 80:
            return "Elevated"
        elif sys < 140 or dia < 90:
            return "High BP Stage 1"
        else:
            return "High BP Stage 2"

    df["BP_Status"] = df.apply(
        lambda x: bp_status(
            x["Systolic_BP"],
            x["Diastolic_BP"]
        ),
        axis=1
    )

    total_patients = len(df)
    avg_bmi = round(df["BMI"].mean(), 2)
    avg_age = round(df["Age"].mean(), 1)

    bmi_counts = df["BMI_Category"].value_counts()

    plt.figure(figsize=(5,5))
    plt.pie(
        bmi_counts,
        labels=bmi_counts.index,
        autopct="%1.1f%%",
        startangle=90
    )
    plt.title("BMI Category Distribution")
    plt.savefig("static/bmi_chart.png")
    plt.close()

    return render_template(
        "index.html",
        total_patients=total_patients,
        avg_bmi=avg_bmi,
        avg_age=avg_age,
        data=df.to_dict(orient="records"),
        search=search
    )


