@app.route("/edit/<int:patient_id>", methods=["GET", "POST"])
def edit_patient(patient_id):

    conn = sqlite3.connect("health.db")
    cursor = conn.cursor()

    if request.method == "POST":

        cursor.execute("""
            UPDATE patients
            SET Age=?,
                Gender=?,
                Height_cm=?,
                Weight_kg=?,
                Systolic_BP=?,
                Diastolic_BP=?
            WHERE Patient_ID=?
        """, (
            request.form["Age"],
            request.form["Gender"],
            request.form["Height_cm"],
            request.form["Weight_kg"],
            request.form["Systolic_BP"],
            request.form["Diastolic_BP"],
            patient_id
        ))

        conn.commit()
        conn.close()

        return redirect(url_for("home"))

    patient = cursor.execute(
        "SELECT * FROM patients WHERE Patient_ID=?",
        (patient_id,)
    ).fetchone()

    conn.close()

    return render_template("edit.html", patient=patient)

