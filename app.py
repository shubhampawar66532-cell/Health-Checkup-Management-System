@app.route("/download_pdf")
def download_pdf():

    conn = sqlite3.connect("health.db")
    cursor = conn.cursor()

    data = cursor.execute(
        "SELECT * FROM patients"
    ).fetchall()

    conn.close()

    pdf = canvas.Canvas("Health_Report.pdf")

    # Title
    pdf.setFont("Helvetica-Bold", 18)
    pdf.drawString(170, 800, "Health Check-Up Report")

    pdf.setFont("Helvetica", 11)
    pdf.drawString(30, 780, "Generated by Health Check-Up Dashboard")

    pdf.line(20, 770, 580, 770)

    # Table Heading
    pdf.setFont("Helvetica-Bold", 9)

    pdf.drawString(20, 750, "ID")
    pdf.drawString(60, 750, "Age")
    pdf.drawString(95, 750, "Gender")
    pdf.drawString(150, 750, "Height")
    pdf.drawString(220, 750, "Weight")
    pdf.drawString(290, 750, "Sys BP")
    pdf.drawString(350, 750, "Dia BP")
    pdf.drawString(420, 750, "BMI")
    pdf.drawString(470, 750, "BP")

    y = 730

    pdf.setFont("Helvetica", 9)

    # Patient Data
    for row in data:

        height = row[3] / 100
        bmi = round(row[4] / (height * height), 2)

        if row[5] < 120 and row[6] < 80:
            bp = "Normal"
        elif row[5] < 130 and row[6] < 80:
            bp = "Elevated"
        elif row[5] < 140 or row[6] < 90:
            bp = "Stage1"
        else:
            bp = "Stage2"

        pdf.drawString(20, y, str(row[0]))
        pdf.drawString(60, y, str(row[1]))
        pdf.drawString(95, y, str(row[2]))
        pdf.drawString(150, y, str(row[3]))
        pdf.drawString(220, y, str(row[4]))
        pdf.drawString(290, y, str(row[5]))
        pdf.drawString(350, y, str(row[6]))
        pdf.drawString(420, y, str(bmi))
        pdf.drawString(470, y, bp)

        y -= 20

        if y < 50:
            pdf.showPage()
            pdf.setFont("Helvetica", 9)
            y = 800

    pdf.save()

    return send_file(
        "Health_Report.pdf",
        as_attachment=True
    )
