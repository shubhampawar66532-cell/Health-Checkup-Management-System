    ws = wb.active
    ws.title = "Patients"

    ws.append([
        "Patient ID",
        "Age",
        "Gender",
        "Height (cm)",
        "Weight (kg)",
        "Systolic BP",
        "Diastolic BP"
    ])

    for row in data:
        ws.append(row)

    wb.save("Health_Report.xlsx")

    return send_file(
        "Health_Report.xlsx",
        as_attachment=True
    )
